            <h2>Key Features:</h2>
            <ul>
                <li><strong>Awareness Campaigns</strong>: Organizing events, workshops, and seminars to raise awareness about mental health and reduce stigma.</li>
                <li><strong>Counseling Services</strong>: Offering access to professional counseling and therapy for students experiencing mental health challenges.</li>
                <li><strong>Peer Support Programs</strong>: Training students to become peer supporters, offering guidance and a listening ear to fellow students in need.</li>
                <li><strong>Online Resources</strong>: Providing digital resources, such as mental health apps, articles, and videos, to help students manage stress and anxiety.</li>
            </ul>
            <br>
            <h2>Impact:</h2>
            <h5>Through these initiatives, the program has been able to create a positive shift in how students view and address mental health. By promoting open conversations and offering accessible resources, the program has helped reduce stress, anxiety, and depression among students, leading to better academic performance and overall well-being.</p>
            <br>
            <h2>Success Stories:</h2>
            <ul>
                <li><strong>Awareness Campaign in High Schools</strong>: A campaign conducted in several high schools helped reduce mental health stigma and encouraged over 200 students to seek counseling.</li>
                <li><strong>Peer Support Program at University</strong>: A university peer support initiative led to improved mental health outcomes for participating students, who reported feeling more supported and less isolated.</li>
