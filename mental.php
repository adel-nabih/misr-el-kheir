            <h2>Our Mission:</h2>
            <h4>The Student Mental Health Outreach program aims to raise awareness about mental health issues and provide resources for students to manage their mental well-being. The program focuses on creating a supportive environment where students feel empowered to seek help when needed, fostering a culture of mental health awareness on campuses.</p>
